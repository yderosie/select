#include "libft.h"

char	*ft_strcat(char *s1, char const *s2)
{
	int		i;
	int		j;
	char	*s;

	s = (void *)s2;
	i = ft_strlen(s1);
	j = 0;
	while (s[j] != '\0')
	{
		s1[i] = s[j];
		i++;
		j++;
	}
	s1[i] = '\0';
	return (s1);
}
